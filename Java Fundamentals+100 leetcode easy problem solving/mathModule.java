import java.io.*;

class mathModule {
    public static void main(String[] args) {
        Math.max(5, 10);    // Maximum
        Math.min(5, 10);    // Minimum
        Math.sqrt(64);      // Square
        Math.abs(-4.7);     // Give only Positive value
        Math.pow(2, 8);     // Return Power value
        Math.round(4.6);    // Return a Round figure
        Math.ceil(4.1);
        Math.floor(4.9);
    }
}
